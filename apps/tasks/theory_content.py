    '| `--force` / `-f` | Перезаписать remote (опасно на shared ветках) |\n'
    '| `--force-with-lease` | Force только если remote не менялся «сзади» |\n'
    '| `--dry-run` | Проверка без отправки |\n'
    '| `--all` | Все ветки |\n'
