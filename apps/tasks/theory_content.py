    '\n'
    '## `git push` — отправить коммиты на сервер\n'
    '\n'
    '**Назначение:** передать ваши коммиты на remote, обновить ветку там.\n'
    '\n'
    '```bash\n'
    'git push -u origin feature-x\n'
    'git push origin main\n'
    'git push\n'
    'git push origin --delete old-feature\n'
    'git push --tags\n'
    'git push origin v1.0.0\n'
    'git push --force-with-lease origin feature-x\n'
