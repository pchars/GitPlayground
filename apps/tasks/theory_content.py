    '## `git hash-object` — как Git хранит файл\n'
    '\n'
    'Связь с уровнем 8: каждый файл в коммите — **blob** с SHA.\n'
    '\n'
    '```bash\n'
    'echo "api" > api.txt\n'
    'git hash-object api.txt           # только SHA, без записи\n'
    'git hash-object -w api.txt        # записать blob в .git/objects\n'
    'git cat-file -p $(git hash-object api.txt)\n'
    '```\n'
    '\n'
    '| Флаг | Описание |\n'
    '| --- | --- |\n'
    '| `-w` | Write в object database |\n'
    '| `--stdin` | Hash из stdin |\n'
    '| `-t blob` | Явно тип blob |\n'
    '\n'
    'Полезно для понимания: **имя файла** живёт в tree, **содержимое** — в blob.\n'
