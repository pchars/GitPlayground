    '| Флаг / форма | Описание |\n'
    '| --- | --- |\n'
    '| `-u` / `--set-upstream` | Запомнить связь ветки с remote (первый push) |\n'
    '| `<remote> <ветка>` | Явно: куда пушить |\n'
    '| `<remote> <local>:<remote-branch>` | Локальная ветка → имя на сервере |\n'
    '| `--delete` / `-d` | Удалить ветку на remote |\n'
    '| `--tags` | Отправить все теги |\n'
