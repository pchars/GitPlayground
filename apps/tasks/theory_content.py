    '\n'
    'Pipeline запускается на **push** и при **Merge Request**.\n'
    '\n'
    '### Минимальный пример\n'
    '\n'
    '```yaml\n'
    'stages:\n'
    '  - test\n'
    '  - deploy\n'
    '\n'
    'test_job:\n'
    '  stage: test\n'
    '  script:\n'
    '    - echo "Running unit tests"\n'
    '    - echo ok\n'
    '\n'
    'pages:\n'
    '  stage: deploy\n'
    '  script:\n'
    '    - echo "Build static site"\n'
    '  only:\n'
    '    - main\n'
    '```\n'
    '\n'
    '| Ключ | Описание |\n'
    '| --- | --- |\n'
    '| `stages` | Порядок этапов pipeline |\n'
    '| `stage` | К какому этапу относится job |\n'
    '| `script` | Команды shell (как в терминале) |\n'
    '| `image` | Docker-образ runner (например `python:3.12`) |\n'
    '| `only` / `rules` | Когда запускать job |\n'
    '| `artifacts` | Файлы между job / для скачивания |\n'
    '| `cache` | Ускорение повторных сборок |\n'
    '\n'
    '```bash\n'
    '# создать .gitlab-ci.yml, затем:\n'
    'git add .gitlab-ci.yml\n'
