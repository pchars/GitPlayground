    '## `git stash` — временное хранилище правок\n'
    '\n'
    '**Назначение:** убрать незакоммиченные изменения из рабочей папки, чтобы переключить\n'
    'ветку или подтянуть `pull`, а потом вернуть правки.\n'
    '\n'
    '```bash\n'
    'git stash push -m "WIP: форма регистрации"\n'
    'git stash list\n'
    'git stash show -p stash@{0}\n'
    'git stash apply              # применить, stash остаётся\n'
    'git stash pop                # применить и удалить из списка\n'
    'git stash drop stash@{0}\n'
    'git stash clear              # удалить все stash (осторожно)\n'
    '```\n'
    '\n'
    '| Команда / флаг | Описание |\n'
    '| --- | --- |\n'
    '| `push` | Сохранить текущие изменения в stash |\n'
    '| `push -m "msg"` | Сообщение для списка stash |\n'
    '| `push -u` / `--include-untracked` | Включить **untracked** файлы |\n'
    '| `push -a` / `--all` | Включить и ignored (редко) |\n'
    '| `push -- path/` | Спрятать только указанные пути |\n'
    '| `list` | Все записи: `stash@{0}`, `stash@{1}`, … |\n'
    '| `show` | Краткая сводка diff |\n'
    '| `show -p` | Полный patch |\n'
    '| `apply stash@{n}` | Применить без удаления |\n'
    '| `pop` | Apply + drop верхнего |\n'
    '| `branch <имя>` | Создать ветку из stash |\n'
    '| `drop` | Удалить запись |\n'
    '\n'
    '**Типичный сценарий:**\n'
    '\n'
    '```bash\n'
    '# на feature, незакоммиченные правки\n'
    'git stash push -m "half-done feature"\n'
    'git switch main\n'
    'git pull\n'
    'git switch feature\n'
    'git stash pop\n'
    '# если конфликт при pop — разрешить как при merge\n'
    '```\n'
    '\n'
    '**Что stash сохраняет по умолчанию:** изменения **отслеживаемых** файлов (staged и '
    'unstaged).\n'
    'Untracked — только с `-u`.\n'
    '\n'
    '---\n'
    '\n'
    '## `git reset` — сдвинуть указатель ветки\n'
    '\n'
    '**Назначение:** переместить ветку (например `main`) на другой коммит. Три режима\n'
    'по-разному трогают **индекс** и **рабочую папку**.\n'
    '\n'
    '```bash\n'
    'git reset --soft HEAD~1\n'
    'git reset HEAD~1              # то же, что --mixed (по умолчанию)\n'
    'git reset --hard HEAD~1\n'
    'git reset --hard abc1234\n'
    'git reset HEAD file.txt       # снять один файл с индекса\n'
    '```\n'
    '\n'
    '### Три режима (запомните таблицу)\n'
    '\n'
    '| Режим | Куда двигается ветка | Индекс (staging) | Рабочая папка |\n'
    '| --- | --- | --- | --- |\n'
    '| `--soft` | Да | **Сохраняет** staged | **Сохраняет** |\n'
    '| `--mixed` (default) | Да | **Очищает** | **Сохраняет** |\n'
    '| `--hard` | Да | **Очищает** | **Стирает** до состояния коммита |\n'
    '\n'
    '### Формы указания «куда откатываться»\n'
    '\n'
    '| Форма | Значение |\n'
    '| --- | --- |\n'
    '| `HEAD~1` | На один коммит назад по текущей ветке |\n'
    '| `HEAD~3` | На три коммита назад |\n'
    '| `HEAD^` | Родитель HEAD (для merge — первый родитель) |\n'
    '| `<SHA>` | Конкретный коммит |\n'
    '| `<ветка>` | Состояние другой ветки |\n'
    '\n'
    '### Примеры по режимам\n'
    '\n'
    '**`--soft`:** «хочу переделать сообщение/состав, но всё оставить в индексе»\n'
    '\n'
    '```bash\n'
    'git reset --soft HEAD~1\n'
    'git commit -m "Один нормальный коммит вместо двух WIP"\n'
    '```\n'
    '\n'
    '**`--mixed`:** «отменить коммит, правки пусть останутся unstaged»\n'
    '\n'
    '```bash\n'
    'git reset HEAD~1\n'
    'git status    # изменённые файлы на месте, не в индексе\n'
    '```\n'
    '\n'
    '**`--hard`:** «выкинуть и коммит, и правки» (опасно)\n'
    '\n'
    '```bash\n'
    'git reset --hard HEAD~1\n'
    '# незакоммиченное тоже пропадёт!\n'
    '```\n'
    '\n'
    'Перед `--hard`: запишите `git log -1` или `git branch backup-before-reset`.\n'
    '\n'
    '### Восстановление после ошибки\n'
    '\n'
    'Коммиты после reset ещё какое-то время видны в **`git reflog`** (уровень 8):\n'
    '\n'
    '```bash\n'
    'git reflog\n'
    'git reset --hard HEAD@{2}\n'
    '```\n'
    '\n'
    '---\n'
    '\n'
    '## Сводная шпаргалка уровня\n'
    '\n'
    '| Задача | Команды |\n'
    '| --- | --- |\n'
    '| Исправить сообщение последнего коммита | `git commit --amend -m "..."` |\n'
    '| Объединить несколько коммитов | `git rebase -i HEAD~N` + squash |\n'
    '| Поменять порядок коммитов | `git rebase -i` + переставить строки |\n'
