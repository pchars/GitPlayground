    '## `git fetch` — скачать без слияния\n'
    '\n'
    '**Назначение:** получить новые коммиты и ветки с remote, **не меняя** текущую рабочую ветку.\n'
    '\n'
    '```bash\n'
    'git fetch origin\n'
    'git fetch origin main\n'
    'git fetch --all\n'
    'git fetch --prune\n'
    'git fetch origin feature-x:feature-x\n'
    '```\n'
    '\n'
    '| Флаг / форма | Описание |\n'
    '| --- | --- |\n'
    '| `<remote>` | С какого remote качать |\n'
    '| `<remote> <refspec>` | Обновить конкретную ветку |\n'
    '| `--all` | Все remotes |\n'
    '| `--prune` / `-p` | Удалить local remote-tracking refs, которых нет на сервере |\n'
    '| `--dry-run` | Показать, что бы скачалось |\n'
    '| `--depth <n>` | Углубить shallow clone |\n'
    '| `--tags` | Скачать теги (или `--no-tags`) |\n'
    '| `-v` / `--verbose` | Подробный вывод |\n'
    '\n'
    '**После fetch — посмотреть, что нового:**\n'
    '\n'
    '```bash\n'
